package tema_exercitiu3_employee;

public class Employee {

    String nume;
    int anul_inscrierii;
    int salariu;
    String adresa;

    public Employee(String nume, int anul_inscrierii, int salariu, String adresa) {
        this.nume = nume;
        this.anul_inscrierii = anul_inscrierii;
        this.salariu = salariu;
        this.adresa = adresa;
    }
}
